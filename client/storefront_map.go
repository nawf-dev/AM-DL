func storefrontCodeFromStorefrontID(storefrontID string) string {
	prefix := strings.TrimSpace(storefrontID)
	if prefix == "" {
		return ""
	}
	if idx := strings.Index(prefix, "-"); idx >= 0 {
		prefix = prefix[:idx]
	}
	if idx := strings.Index(prefix, ","); idx >= 0 {
		prefix = prefix[:idx]
	}
	return storefrontCodeByNumericID[prefix]
}
